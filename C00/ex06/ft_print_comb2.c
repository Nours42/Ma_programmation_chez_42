#include <unistd.h>

void	ft_putchar(char c)
{
	write (1, &c, 1);
}

void	display(int nbr)
{
	int	first;
	int	second;

	if (nbr > 9)
	{
		first = nbr / 10;
		second = nbr % 10;
		ft_putchar(first + 48);
		ft_putchar(second + 48);
	}
	else
	{
		ft_putchar('0');
		ft_putchar(nbr + 48);
	}
}

void	ft_print_comb2(void)
{
	int	a;
	int	b;

	a = 0;
	while (a <= 99)
	{
		b = a + 1;
		while (b <= 99)
		{
			display(a);
			ft_putchar(' ');
			display(b);
			if (a < 98 || b < 99)
			{
				ft_putchar(',');
				ft_putchar(' ');
			}
			b++;
		}
		a++;
	}
}

