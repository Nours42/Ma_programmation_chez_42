int	ft_str_is_alpha(char *str)
{
	int	i;
	int	j;

	i = 0;
	while (str[i])
	{
		j = str[i];
		if (!(((j >= 'A') && (j <= 'Z')) || ((j >= 'a') && (j <= 'z'))))
			return (0);
		i++;
	}
	return (1);
}
