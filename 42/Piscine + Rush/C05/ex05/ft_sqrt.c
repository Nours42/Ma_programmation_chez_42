int	ft_sqrt(int nb)
{
	int	i;
	int	r;

	r = 1;
	i = 0;
	if (nb < 0)
		return (0);
	if (nb == 0 || nb == 1)
		return (nb);
	while (r <= 46341)
	{
		i = r * r;
		if (i == nb)
		{
			return (r);
		}
		r++;
	}
	return (0);
}
