int	ft_str_is_printable(char *str)
{
	int	i;
	int	j;

	i = 0;
	while (str[i])
	{
		j = str[i];
		if (!((j >= 32) && (j <= 126)))
			return (0);
		i++;
	}
	return (1);
}
